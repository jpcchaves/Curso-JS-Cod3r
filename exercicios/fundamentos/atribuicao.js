//OPERADORES DE ATRIBUIÇÃO

const a = 7; // atribui 7 a variável "a"
let b = 3; // atribui 3 a varoável "b"

//atribuição aditiva - acrescenta o valor de "a" ao valor de "b"
b += a; // b = b + a
console.log(b)
    
//atribuição subtrativa
b -= 4; // b = b - 4
console.log(b)

//atribuição multiplicativa
b *= 2 // b = b * 2
console.log(b)

//atribuição dividitiva
b /= 2; // b = b/2
console.log(b);

//atribuição modular - recebe o resto da divisão
b %= 2; // b = b%2 
console.log(b) // o resto da divisão de 6/2 = 0